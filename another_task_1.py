types = {
    1: 'Блокирующий',
    2: 'Критический',
    3: 'Значительный',
    4: 'Незначительный',
    5: 'Тривиальный'
}

tickets = {
    1: ['API_45', 'API_76', 'E2E_4'],
    2: ['UI_19', 'API_65', 'API_76', 'E2E_45'],
    3: ['E2E_45', 'API_45', 'E2E_2'],
    4: ['E2E_9', 'API_76'],
    5: ['E2E_2', 'API_61']
}

seen = set()
result = {}

for severity in sorted(tickets):
    unique = []
    for ticket in tickets[severity]:
        if ticket not in seen:
            seen.add(ticket)
            unique.append(ticket)
    result[types[severity]] = unique

print(result)